package network

import (
	"bufio"
	"bytes"
	"testing"
)

// Very ad hoc test based on status response packet
func TestPacketEncoder(t *testing.T) {
	var buf bytes.Buffer
	enc := NewPacketEncoder(bufio.NewWriter(&buf))

	ok := enc.WriteVarInt(0x00) &&
		enc.WriteString(`{"version":{"name":"1.7.6-1.8.9","protocol":47},"players":{"max":0,"online":0},"description":{"text":"Hello world"}}`) &&
		enc.WritePacket(true)
	if !ok {
		t.Error(enc.LastError())
	}

	expected := []byte{
		0x76, 0x00, 0x74, 0x7B, 0x22, 0x76, 0x65, 0x72, 0x73, 0x69, 0x6F, 0x6E, 0x22, 0x3A, 0x7B, 0x22, 0x6E, 0x61,
		0x6D, 0x65, 0x22, 0x3A, 0x22, 0x31, 0x2E, 0x37, 0x2E, 0x36, 0x2D, 0x31, 0x2E, 0x38, 0x2E, 0x39, 0x22, 0x2C,
		0x22, 0x70, 0x72, 0x6F, 0x74, 0x6F, 0x63, 0x6F, 0x6C, 0x22, 0x3A, 0x34, 0x37, 0x7D, 0x2C, 0x22, 0x70, 0x6C,
		0x61, 0x79, 0x65, 0x72, 0x73, 0x22, 0x3A, 0x7B, 0x22, 0x6D, 0x61, 0x78, 0x22, 0x3A, 0x30, 0x2C, 0x22, 0x6F,
		0x6E, 0x6C, 0x69, 0x6E, 0x65, 0x22, 0x3A, 0x30, 0x7D, 0x2C, 0x22, 0x64, 0x65, 0x73, 0x63, 0x72, 0x69, 0x70,
		0x74, 0x69, 0x6F, 0x6E, 0x22, 0x3A, 0x7B, 0x22, 0x74, 0x65, 0x78, 0x74, 0x22, 0x3A, 0x22, 0x48, 0x65, 0x6C,
		0x6C, 0x6F, 0x20, 0x77, 0x6F, 0x72, 0x6C, 0x64, 0x22, 0x7D, 0x7D,
	}
	b := buf.Bytes()
	if !bytes.Equal(expected, b) {
		t.Errorf("Expected %v, got %v", expected, b)
	}
}
